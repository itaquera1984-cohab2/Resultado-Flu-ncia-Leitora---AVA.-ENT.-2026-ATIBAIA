const lines = pindaRaw.trim().split('\n');
const parsePct = (s) => parseFloat(s.replace('%', '').replace(',', '.'));

const classesPinda = lines.map(line => {
    const p = line.split(';');
    return {
        escola: p[1].trim(),
        turma: p[2].trim(),
        previstos: parseInt(p[3]),
        avaliados: parseInt(p[4]),
        participacao: parseFloat(p[5].replace(',', '.')),
        preTotal: parsePct(p[6]),
        n1: parsePct(p[7]),
        n2: parsePct(p[8]),
        n3: parsePct(p[9]),
        n4: parsePct(p[10]),
        iniciante: parsePct(p[11]),
        fluente: parsePct(p[12])
    };
});

const aggPinda = {};
