    if (!aggPinda[c.escola]) aggPinda[c.escola] = { p: 0, a: 0, n1: 0, n2: 0, n3: 0, n4: 0, li: 0, lf: 0 };
    const s = aggPinda[c.escola];
    s.p += c.previstos;
    s.a += c.avaliados;
    s.n1 += (c.n1 * c.avaliados) / 100;
    s.n2 += (c.n2 * c.avaliados) / 100;
    s.n3 += (c.n3 * c.avaliados) / 100;
